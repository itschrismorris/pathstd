/**/
template <typename T>
static inline utf8* from_int(T value, 
                             utf8* buffer,
                             u32* length_out = nullptr)
{
  u64 v = Math::abs(value);
  utf8* output;
  if constexpr (sizeof(T) == 4) {
    output = &buffer[11];
  } else {
    output = &buffer[21];
  }
  *output = '\0';
  while (v >= 100) {
    u64 const old = v;
    output -= 2;
    v /= 100;
    Memory::memcpy(output, &two_digits[old - (v * 100)], sizeof(u16));
  }
  output -= 2;
  Memory::memcpy(output, &two_digits[v], sizeof(u16));
  bool is_single_digit = (v < 10);
  output += is_single_digit;
  if (value < 0) {
    output[-1] = '-';
    output -= 1;
  }
  if (length_out) {
    if constexpr (sizeof(T) == 4) {
      *length_out = &buffer[11] - output;
    } else {
      *length_out = &buffer[21] - output;
    }
  }
  return output;
}
